package class11;

public class UpperLowerCaseDemo {
	

	public static void main(String[] args) {
		

		String str1="Hello Welcome To Happy World";
		
		System.out.println(str1);
		
		String lowerCaseString=str1.toLowerCase();
		System.out.println(lowerCaseString);
		
		System.out.println(str1.toLowerCase()); 	// Second way
	
		System.out.println(str1.toUpperCase());
		System.out.println(lowerCaseString.toUpperCase());	// Second way


		
	}

}
